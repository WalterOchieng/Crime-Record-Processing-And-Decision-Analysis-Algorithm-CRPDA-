    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        
         
         if(txtLocationID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Location ID","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtLocationID.requestFocus();
            
        }
        else  if(txtLocationName.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter  Location  Name ","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtLocationName.requestFocus();
            
            
            
        }
        
        else  if(txtDescription.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter  Description of Location ","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtDescription.requestFocus();
            
            
            
        }
        else 
        {
        
        
        

        try {
            String sql = "Insert  into locations(LocationID,LocationName,Description)values(?,?,?)";
            pst = conn.prepareStatement(sql);

            pst.setString(1, txtLocationID.getText());
            pst.setString(2, txtLocationName.getText());
            pst.setString(3, txtDescription.getText());
           

            pst.execute();

            JOptionPane.showMessageDialog(null, "Saved Succesfully");

        } 
        catch (SQLException er) {
            
            JOptionPane.showMessageDialog(null, "The Data is already Saved you cannot save it again","CRPDS Message",JOptionPane.INFORMATION_MESSAGE);
        }
        
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

