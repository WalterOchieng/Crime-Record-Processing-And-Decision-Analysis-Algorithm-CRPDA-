    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

        try {
            String sql = "select * from  locations where LocationID=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtSearch.getText());

            rs = pst.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("LocationID");
                txtLocationID.setText(add1);
                String add2 = rs.getString("LocationName");
                txtLocationName.setText(add2);
                String add3 = rs.getString("Description");
                txtDescription.setText(add3);
                

                String addSrch = rs.getString("LocationID");
                txtSearch1.setText(addSrch);

            }
            
               else
            {
                JOptionPane.showMessageDialog(null,"The Record has not been found","CRPDAS Message",JOptionPane.INFORMATION_MESSAGE);
            }


        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

         if(txtLocationID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Location ID","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtLocationID.requestFocus();
            
        }
        else  if(txtLocationName.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter  Location  Name ","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtLocationName.requestFocus();
            
            
            
        }
        
        else  if(txtDescription.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter  Description of Location ","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtDescription.requestFocus();
            
            
            
        }
        else 
        {
        
        try {

            String value1 = txtLocationID.getText();
            String value2 = txtLocationName.getText();
            String value3 = txtDescription.getText();
            

            String searchn = txtSearch1.getText();

            String sql = "update locations set LocationID='" + value1 + "',LocationName='" + value2 + "',Description='" + value3 +  "' where LocationID='" + searchn + "' ";
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Updated");

        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void cmd_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_deleteActionPerformed

        
        
         if(txtLocationID.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please Type The Location ID  you want to delete ");
        }
        else{
        
        int n=JOptionPane.showConfirmDialog(null, "Are you sure you want to delete","CRPDAS Message  ",JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION)
        {
        String sql = "delete from locations where LocationID=?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtLocationID.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Deleted");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        }
         }
         
    }//GEN-LAST:event_cmd_deleteActionPerformed

