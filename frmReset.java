    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        // TODO add your handling code here:
        
          if(txtIstaffNo.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter The Staff ID","CRPDAS Message",JOptionPane.INFORMATION_MESSAGE);
            txtIstaffNo.requestFocus();
            
        }
        else  if(txtSecretKey.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter your Secret Word/Key ","CRPDAS Message",JOptionPane.INFORMATION_MESSAGE);
            txtSecretKey.requestFocus();
    
            
        }
        
       
        else 
        {
       
         String sql="select * from staff where UserID=? AND Status=1 and SecretWord=?";
        
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setString(1,txtIstaffNo.getText());
            pst.setString(2, txtSecretKey.getText());
            
            
            rs=pst.executeQuery();
            
            if(rs.next())
            {
                
                
               // JOptionPane.showMessageDialog(null,"Login succesfull!!!!");
                dispose();
                frmReset2 s =new frmReset2();               
        s.txtUserID.setText(this.txtIstaffNo.getText());
         s.setVisible(true);
                        
            }
            
            else 
            {
                JOptionPane.showMessageDialog(null,"Your ID or Secret Key is Inccorect ");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        }
        
    }//GEN-LAST:event_btnResetActionPerformed

