    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        // TODO add your handling code here:
        
         if(txtNewPasswd.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter the new Password","CRPDAS Message",JOptionPane.INFORMATION_MESSAGE);
            txtNewPasswd.requestFocus();
            
        }
        else  if(txtNewPasswdConf.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Confirm Your Password ","CRPDAS Message",JOptionPane.INFORMATION_MESSAGE);
            txtNewPasswdConf.requestFocus();
    
            
        }
        
       
        else 
        {

        try {

            String value1 = txtStaffID.getText();
            String value2 = txtNewPasswd.getText();
            String value3=   txtsetby.getText();

            String sql = "update staff set Password='"+value2 + "',Staff_No='"+value1 + "',setby='"+value3+"' where Staff_No='" + value1+ "' ";
            pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, " Password Updated");

        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
        
        }
    }//GEN-LAST:event_btnResetActionPerformed

