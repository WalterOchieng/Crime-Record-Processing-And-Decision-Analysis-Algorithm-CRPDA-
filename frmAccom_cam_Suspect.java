    private void tctAccompKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tctAccompKeyTyped
        // TODO add your handling code here:

        if(   (!(Character.isDigit(evt.getKeyChar()))) &&  (!(Character.isSpaceChar(evt.getKeyChar()))) && (evt.getKeyChar()!=KeyEvent.VK_BACK_SPACE)  &&(evt.getKeyChar()!=KeyEvent.VK_DELETE)&& (evt.getKeyChar()!=KeyEvent.VK_ESCAPE)  )
        {
            evt.consume();

            JOptionPane.showMessageDialog(null, "Numbers only","CRPDAS",JOptionPane.INFORMATION_MESSAGE);

        }

        boolean max= tctAccomp.getText().length()>15;
        if(max)
        {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limit of charcters reached ","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_tctAccompKeyTyped

