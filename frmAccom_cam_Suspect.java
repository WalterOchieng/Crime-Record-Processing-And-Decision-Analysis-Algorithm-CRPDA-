    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if(tctAccomp.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Accomplisher ID","CRPDAS Message ",JOptionPane.INFORMATION_MESSAGE);
            tctAccomp.requestFocus();

        }
        else  if(txtSuspectId.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Suspect ID ","CRPDAS Message ",JOptionPane.INFORMATION_MESSAGE);
            txtSuspectId.requestFocus();

        }
    
        else
        {

            int n=JOptionPane.showConfirmDialog(null, "Are you sure you want to Update","CRPDAS Message ",JOptionPane.YES_NO_OPTION);
            if(n==JOptionPane.YES_OPTION)
            {
                try {

                    String value1 = tctAccomp.getText();
                    String value2 = txtSuspectId.getText();
                    

                    String searchn = txtSearch1.getText();

                    String sql = "update  accomplisher_suspect set  Accomplisher_id='" + value1 + "',Accused_ID='" + value2 + "'where Accomplisher_id='" + searchn + "' ";
                    pst = conn.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Updated");

                } catch (SQLException | HeadlessException e) {
                    JOptionPane.showMessageDialog(null, e);
                }}}

    }//GEN-LAST:event_btnUpdateActionPerformed

