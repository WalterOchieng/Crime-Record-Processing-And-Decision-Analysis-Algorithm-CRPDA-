    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtSearch.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Accomplisher ID to search","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtSearch.requestFocus();
        }
        else
        {
            try {
                String sql = "select * from  accomplisher_suspect where Accomplisher_id=?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtSearch.getText());

                rs = pst.executeQuery();

                if (rs.next()) {
                    String add1 = rs.getString("Accomplisher_id");
                    tctAccomp.setText(add1);
                    String add2 = rs.getString("Accused_ID");
                    txtSuspectId.setText(add2);
                   

                    String addSrch = rs.getString("Accomplisher_id");
                    txtSearch1.setText(addSrch);

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

