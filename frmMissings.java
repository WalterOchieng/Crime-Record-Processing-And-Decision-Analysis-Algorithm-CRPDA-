    private void btnSearchedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchedActionPerformed
        // TODO add your handling code here:
        
        
        if(txt_search2.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Missing ID to search","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txt_search2.requestFocus();
        }
        else 
        { 

        try {
            String sql = "select * from  maslenious where mas_no=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_search2.getText());

            rs = pst.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("mas_no");
                txtMisNo.setText(add1);
                String add2 = rs.getString("fname");
                txtFName.setText(add2);
                String add3 = rs.getString("Sname");
                txtSName.setText(add3);
                String add4 = rs.getString("Lname");
                txtLName.setText(add4);
                String add5 = rs.getString("IDNo");
                txtIdNo.setText(add5);
                
                String add6 = rs.getString("txtHeight");
                txtHeight.setText(add6);
                
                
                
                String add8 = rs.getString("Weight");
                txtWeight.setText(add8);
                
                String dateValue2 = rs.getString("last_seen"); // What ever column
                java.util.Date add9 = new SimpleDateFormat("yyyy-MM-dd").parse(dateValue2);
                dtcDate.setDate(add9);
                
                String dateValue3 = rs.getString("DOB"); // What ever column
                java.util.Date add10 = new SimpleDateFormat("yyyy-MM-dd").parse(dateValue3);
                dtcDOB.setDate(add10);
                
                
                byte[]imagedata=rs.getBytes("photo");
                ImageIcon imageicon= new ImageIcon(new ImageIcon(imagedata).getImage().getScaledInstance(lblPhoto.getWidth(),lblPhoto.getHeight(),Image.SCALE_DEFAULT));
                lblPhoto.setIcon(imageicon);

                
                String addcon=rs.getString("Type");
                cboType.setSelectedItem(addcon);
                
              
                String add11 = rs.getString("Reason");
                txtReason.setText(add11);

               

                String searched = rs.getString("mas_no");
                txtSearched.setText(searched);

            }
            
             else
            {
                JOptionPane.showMessageDialog(null,"The Record has not been found","CRPDAS MEssage",JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btnSearchedActionPerformed

