    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
         if(txtSearch.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter Jail Number    to search record ","CRPDAS",JOptionPane.INFORMATION_MESSAGE);
            txtSearch.requestFocus();
        }
        else 
        {
         try {
            String sql = "select * from  jail where JailNo=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtSearch.getText());

            rs = pst.executeQuery();
            
            if (rs.next()) {
                String add1 = rs.getString("JailNo");
                txtJailNo.setText(add1);
                String add2 = rs.getString("JailName");
                txtJailName.setText(add2);
                String addcon=rs.getString("JailType");
                cboJailType.setSelectedItem(addcon);
                
                
                String addSrch = rs.getString("JailNo");
                txtJailNoSrch.setText(addSrch);

        
                 }
            
             else
            {
                JOptionPane.showMessageDialog(null,"The Record has not been found","CRPDAS Message",JOptionPane.INFORMATION_MESSAGE);
            }


                } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
                }
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

